def get_translation(key: str, lang: str = 'en', **kwargs) -> str:
    """
    Get translation for a key in specified language.
    
    Args:
        key: Translation key
        lang: Language code ('en' or 'es')
        **kwargs: Variables to format into the translation
    
    Returns:
        Translated string
    """
    translation = TRANSLATIONS.get(lang, TRANSLATIONS['en']).get(key, key)
    
    # Format with any provided variables
    if kwargs:
        try:
            translation = translation.format(**kwargs)
        except (KeyError, IndexError):
            pass
    
    return translation


def get_user_language(session_lang: str = None, default: str = 'es') -> str:
    """
    Determine user's preferred language.
    
    Args:
        session_lang: Language stored in session
        default: Default language if none specified
    
    Returns:
        Language code ('en' or 'es')
    """
    if session_lang in ['en', 'es']:
        return session_lang
    return default

